package ch05_package_inheritance.general.myhealth;

public class Human {
    private String name;
    private String gender;
    private double height;
    private double weight;

    public Human(String name, String gender, double height, double weight) {
        this.name = name;
        this.gender = gender;
        this.height = height;
        this.weight = weight;
    }
}
